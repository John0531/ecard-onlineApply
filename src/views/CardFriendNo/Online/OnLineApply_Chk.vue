                            <p>一、增加持卡人之可能負擔。</p>
                            <p>二、提高循環信用利率。</p>
                            <p>三、循環信用利率採浮動式者，變更所選擇之指標利率。</p>
                            <p>四、變更循環信用利息計算方式。</p>
                            <p>五、信用卡使用方式及遺失、被竊或滅失時之處理方式。</p>
                            <p>六、持卡人對他人無權使用其信用卡後所發生之權利義務關係。</p>
                            <p>七、有關信用卡交易帳款疑義之處理程序<span class="red">與涉及持卡人權利義務之信用卡國際組織相關重要規範。</span></p>
                            <p class="red">
                                八、提供持卡人之各項權益、優惠或服務之期間及適用條件。<br>
                                發卡機構至少每季應定期覆核持卡人所適用利率。除有不可歸責於發卡機構之事由而有需於提供期間內調整之情形外，或發卡機構已公告或通知之持卡人權益、優惠或服務之期間及適用條件外，發卡機構得每季調整持卡人所適用利率、向持卡人收取之年費、各項手續費、循環信用利息及違約金等之計算方式及可能負擔之一切費用、其他持卡人權益、優惠或服務之期間及適用條件。
                            </p>
                            發卡機構依第一項或第二項通知持卡人變更契約約款時，如持卡人於異議期限內表示異議，並因而終止契約者，發卡機構對於使用循環信用方式或分期付款方式繳款之持卡人，應給予至少六期之緩衝期，但原分期付款剩餘期數小於六期者，依原契約繼續履行。原信用卡契約第四、十四、十五、二十至二十三、二十五至二十九條約定，因繼續履行契約之需要，對發卡機構與持卡人依然有效。
