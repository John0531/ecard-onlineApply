                    </div>
                    <div>
                        第二十二條（信用卡使用之限制）
                        <div class="l-two-em">
                            持卡人<span class="red">如有下列事由之一者，發卡機構無須事先通知或催告，得降低持卡人之信用額度、調整循環信用最低應繳比率或金額或暫時停止持卡人使用信用卡之權利，並應立即通知持卡人：</span>
                            <p>一、持卡人於申請時所填寫或提出之文件不實，或未於信用卡上簽名或將信用卡之占有移轉，或與他人或特約商店偽造虛構不實交易行為或共謀詐欺而以信用卡簽帳方式或其他方式折換金錢或取得利益，或以信用卡向未經主管機關核准或非各信用卡組織委託辦理預借現金之機構或向他人直接或間接取得資金融通。
                            </p>
                            <p>二、持卡人因故意或重大過失而將使用自動化設備辦理預借現金或進行其他交易之交易密碼或其他辨識持卡人同一性之方式告知他人者。</p>
                            <p>三、持卡人連續二期所繳付款項未達發卡機構所定最低應繳金額者。</p>
                            <p>四、持卡人<span class="red">受監護宣告、輔助宣告</span>或依法聲請和解、破產、更生、清算、前置協商、前置調解、公司重整或經票據交換所宣告拒絕往來或停止營業、清理債務者。
                            </p>
                            <p>五、持卡人為法人或非法人團體之法定代理人、代表人、管理人者，關於該法人或非法人團體經票據交換所公告拒絕往來者。</p>
                            <p><span class="red">六、持卡人因刑事而受有期徒刑以上之宣告或沒收主要財產之宣告者。</span> </p>
                            <span class="red">持卡人如有下列事由之一者，經發卡機構事先通知或催告（包含電話、傳真、電子郵件、簡訊、書面及其他持卡人同意之方式），且持卡人無法釋明正當理由，得降低持卡人之信用額度、調整循環信用最低應繳比率或金額。情節重大時，得暫時停止持卡人使用信用卡之權利：</span>
                            <p>一、持卡人違反第二條第二項，發卡機構已依原申請時填載資料之聯絡方式通知而無法取得聯繫者。</p>
                            <p>二、持卡人有一期所繳付款項未達發卡機構所定最低應繳金額者。</p>
                            <p>三、持卡人超過信用額度使用信用卡交易者。</p>
                            <p>四、持卡人存款不足而退票，<span class="red">或其為法人或非法人團體之法定代理人、代表人、管理人，而該法人或非法人團體存款不足而退票者。</span>
                            </p>
                            <p>五、持卡人因本條第一項事由遭其他發卡機構暫停使用信用卡之權利或終止信用卡契約者。</p>
                            <p>六、持卡人主要財產受強制執行或<span class="red">假扣押、假處分或其他保全處分者</span>。 </p>
                            <p>七、持卡人因<span class="red">其他債務</span>、稅務關係被提起訴訟、<span class="red">調查</span>，或因涉及財產犯罪遭刑事<span class="red">偵查</span>或起訴者。</p>
                            <p>八、<span class="red">持卡人依約定負有提供擔保之義務而不提供者。</span></p>
                            <p>九、<span class="red">對發卡機構（包括總機構及分支機構）或其他金融機構有任一債務逾期未償還、有任一債務有遲延繳納本金或利息者，或由親屬、第三人代為處理債務者，或於財團法人金融聯合徵信中心有不良債信紀錄、授信異常者。</span>
                            </p>
                            <p>十、<span class="red">持卡人職業、職務、經濟來源或舉債情形（包含但不限於各金融機構或發卡機構所核發信用卡、現金卡及其他消費性貸款之總額度與往來之狀況）有所變動，有具體事實足供發卡機構降低原先對持卡人信用之評估者或足認持卡人有信用貶落或債務不履行之虞者。</span>
                            </p>
                            <span class="red">發卡機構得為保障持卡人交易安全或維護發卡機構權益，於持卡人有消費異常情形、接獲國際組織或其他銀行通報或發卡機構認為必要之時，於通知持卡人後，就持卡人之信用卡進行控管，包括暫時停卡、永久停卡或主動換卡等作業，持卡人如不願配合者，得終止本契約。<br>持卡人如疑似刷卡採購商品以融資變現、於自己服務之商店或疑似風險特約商店進行非真實消費之刷卡變現行為、至財團法人聯合信用卡處理中心列管之風險特約商店刷卡消費、或有其他異常簽帳時間、地點、項目而可疑有虛偽不實交易或共謀詐欺之情形，或以信用卡為支付工具進行網際網路賭博等不法交易時，發卡機構亦得比照前項方式辦理。<br>前二項情形，如發卡機構無法即時通知持卡人者，持卡人同意發卡機構得先進行控管再通知持卡人。</span><br>發卡機構於第一項或第二項各款事由消滅後，或經發卡機構同意持卡人釋明之理由，或持卡人清償部分款項或提供適當之擔保者，得恢復原核給持卡人之信用額度之全部或一部、原循環信用最低應繳比率或金額或使用信用卡之權利。<br>發卡機構依第一項或第二項調整持卡人循環信用最低應繳比率或金額時，應考慮持卡人過去繳款情形，酌定適當比率或金額。持卡人如有異議，除有第一項第一款或第二款之情形外，發卡機構與持卡人應本誠信原則協商之。
